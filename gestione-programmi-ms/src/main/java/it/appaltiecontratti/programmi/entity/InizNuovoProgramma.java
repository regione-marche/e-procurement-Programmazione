package it.appaltiecontratti.programmi.entity;

public class InizNuovoProgramma {


    private Boolean showUfficio;
    private String denomUfficio;
    private String idUfficio;
		
	public Boolean getShowUfficio() {
		return showUfficio;
	}
	public void setShowUfficio(Boolean showUfficio) {
		this.showUfficio = showUfficio;
	}
	public String getDenomUfficio() {
		return denomUfficio;
	}
	public void setDenomUfficio(String denomUfficio) {
		this.denomUfficio = denomUfficio;
	}
	public String getIdUfficio() {
		return idUfficio;
	}
	public void setIdUfficio(String idUfficio) {
		this.idUfficio = idUfficio;
	}
}
