package it.appaltiecontratti.tabellati.entity;

public class MessageRecForm {
	
	private Long id;
	private Long sysconReceiver;
	private Long recipientId;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getSysconReceiver() {
		return sysconReceiver;
	}
	public void setSysconReceiver(Long sysconReceiver) {
		this.sysconReceiver = sysconReceiver;
	}
	public Long getRecipientId() {
		return recipientId;
	}
	public void setRecipientId(Long recipientId) {
		this.recipientId = recipientId;
	}
	
	

}
