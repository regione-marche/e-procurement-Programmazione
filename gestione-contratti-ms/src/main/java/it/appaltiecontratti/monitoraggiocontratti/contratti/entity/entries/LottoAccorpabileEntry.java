package it.appaltiecontratti.monitoraggiocontratti.contratti.entity.entries;

public class LottoAccorpabileEntry {
	
	private boolean allowMultiotto;
	
	private boolean hasMultiLotto;

	public boolean isAllowMultiotto() {
		return allowMultiotto;
	}

	public void setAllowMultiotto(boolean allowMultiotto) {
		this.allowMultiotto = allowMultiotto;
	}

	public boolean isHasMultiLotto() {
		return hasMultiLotto;
	}

	public void setHasMultiLotto(boolean hasMultiLotto) {
		this.hasMultiLotto = hasMultiLotto;
	}
	
	

}
