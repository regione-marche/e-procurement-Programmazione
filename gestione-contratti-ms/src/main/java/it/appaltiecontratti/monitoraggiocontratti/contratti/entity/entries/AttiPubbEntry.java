package it.appaltiecontratti.monitoraggiocontratti.contratti.entity.entries;

public class AttiPubbEntry {

	private Long codgara; 
	private Long tipoDoc; 
	private Long numPubb;
	public Long getCodgara() {
		return codgara;
	}
	public void setCodgara(Long codgara) {
		this.codgara = codgara;
	}
	
	public Long getTipoDoc() {
		return tipoDoc;
	}
	public void setTipoDoc(Long tipoDoc) {
		this.tipoDoc = tipoDoc;
	}
	public Long getNumPubb() {
		return numPubb;
	}
	public void setNumPubb(Long numPubb) {
		this.numPubb = numPubb;
	}
	
	
}
