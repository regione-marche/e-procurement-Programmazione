package it.appaltiecontratti.inbox.entity.loaderappalto;

public class DatiPosizioniEntry {

	private String cfAggiudicatario;
	private String codiceCassa;
	private String codiceInps;
	private String codiceInail;
	private String pivimp;
	private Long nazimp;

	public String getCfAggiudicatario() {
		return cfAggiudicatario;
	}

	public void setCfAggiudicatario(String cfAggiudicatario) {
		this.cfAggiudicatario = cfAggiudicatario;
	}

	public String getCodiceCassa() {
		return codiceCassa;
	}

	public void setCodiceCassa(String codiceCassa) {
		this.codiceCassa = codiceCassa;
	}

	public String getCodiceInps() {
		return codiceInps;
	}

	public void setCodiceInps(String codiceInps) {
		this.codiceInps = codiceInps;
	}

	public String getCodiceInail() {
		return codiceInail;
	}

	public void setCodiceInail(String codiceInail) {
		this.codiceInail = codiceInail;
	}

	public String getPivimp() {
		return pivimp;
	}

	public void setPivimp(String pivimp) {
		this.pivimp = pivimp;
	}

	public Long getNazimp() {
		return nazimp;
	}

	public void setNazimp(Long nazimp) {
		this.nazimp = nazimp;
	}

}
